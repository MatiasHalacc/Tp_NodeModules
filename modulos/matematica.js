const pi = 3.14;
function suma(num1Suma, num2Suma){
    return num1Suma + num2Suma;
}

function resta(num1Resta, num2Resta){
    return num1Resta - num2Resta; 
}

const multiplicacion = (num1Multi, num2Multi) => {
    return num1Multi * num2Multi;
} 

const division = (num1Div, num2Div) => {
    return num1Div / num2Div;
} 


export default {suma, resta, multiplicacion, division}


